#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i_s1;
	size_t	i_s2;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	i_s1 = ft_strlen(s1);
	i_s2 = 0;
	while (s2[i_s2])
	{
		s1[i_s1] = s2[i_s2];
		i_s1++;
		i_s2++;
	}
	s1[i_s1] = '\0';
	return (s1);
}
