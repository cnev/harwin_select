#include "libft.h"

char	*ft_strmap(const char *s, char (*f)(char))
{
	char	*out;
	size_t	i;

	if (s == NULL)
		return (NULL);
	out = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	i = 0;
	while (s[i])
	{
		out[i] = f(s[i]);
		i++;
	}
	out[i] = '\0';
	return (out);
}
